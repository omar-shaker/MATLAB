% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

global operation2
global vect1
global vect2
global result
result = 0;

vect1 = sscanf(get(handles.edit4, 'String'), '%f', [1, Inf]);
vect2 = sscanf(get(handles.edit5, 'String'), '%f', [1, Inf]);

set(handles.edit4, 'String', num2str(vect1));
set(handles.edit5, 'String', num2str(vect2));

[vect1Row,vect1Col] = size(vect1);
[vect2Row,vect2Col] = size(vect2);

if (~(vect1Row == 0)) && (~(vect2Row == 0))
    switch operation2
        case 1
            if vect1Col == 1 || vect2Col == 1 || vect1Col == vect2Col
                result = vect1 .* vect2;
                set(handles.text12, 'String', num2str(result));
            else
                set(handles.text12, 'String', "Can't multiply!");
            end
            
        case 2
            if vect1Col == 1 || vect2Col == 1
                result = vect1 * vect2;
                set(handles.text12, 'String', num2str(result));
            else
                set(handles.text12, 'String', "Can't multiply!");
            end
            
        case 3
            if vect1Col == 1 || vect2Col == 1 || vect1Col == vect2Col
                result = vect1 + vect2;
                set(handles.text12, 'String', num2str(result));
            else
                set(handles.text12, 'String', "Can't Add!");
            end

        case 4
            if  vect1Col == 1 || vect2Col == 1 || vect1Col == vect2Col
                result = vect1 - vect2;
                set(handles.text12, 'String', num2str(result));
            else
                set(handles.text12, 'String', "Can't Subtract!");
            end
    end
    
else
    set(handles.text12, 'String', 'Enter both numbers!');
end



