        case 2
            set(handles.text4, 'String', (num1 * num2));

        case 3
            set(handles.text4, 'String', (num1 + num2));

        case 4
            set(handles.text4, 'String', (num1 - num2));
    end
    
else
    set(handles.text4, 'String', 'Enter both numbers!');
end



